#include "3-calc.h"

/**
  * op_add - Addion funtion.
  *
  * @a: Int parameter 1.
  * @b: Int parameter 2.
  *
  * Return: Add a to b.
  */

int op_add(int a, int b)
{
	return (a + b);
}

/**
  * op_sub - Subtraction function.
  *
  * @a: Int parameter 1.
  * @b: Int parameter 2.
  *
  * Return: Subtract a from b.
  */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
  * op_mul - Multiplication function.
  *
  * @a: Int parameter 1.
  * @b: Int parameter 2.
  *
  * Return: Multiply a by b.
  */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
  * op_div - Division function.
  *
  * @a: Int parameter 1.
  * @b: Int parameter 2.
  *
  * Return: Divide a by b.
  */

int op_div(int a, int b)
{
	return (a / b);
}

/**
  * op_mod - Modulo function.
  *
  * @a: Int parameter 1.
  * @b: Int parameter 2.
  *
  * Return: Remainder of the division of a.
  */

int op_mod(int a, int b)
{
	return (a % b);
}
